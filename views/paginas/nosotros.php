<section class="historia contenedor seccion">
    <h2>Historia</h2>
    <h3 style="margin: 0;">Crece un sueño: El esfuerzo de muchos</h3>
    <p>La historia de nuestra empresa constructora comienza hace más de una década,
        cuando un grupo de familiares, impulsados por el deseo de construir un futuro
        mejor, decidieron unir sus esfuerzos y conocimientos en el ámbito de la
        construcción. La confianza mutua y la unión familiar fueron los pilares
        fundamentales que permitieron dar los primeros pasos en este camino. Desde el
        principio, la misión fue clara: ofrecer soluciones de habitabilidad que no solo
        cumplieran con altos estándares de calidad, sino que también respondieran a las
        necesidades de la comunidad. Iniciamos con el negocio que hoy en día se
        denomina “flipping inmobiliario” para nosotros era el ejercicio de comprar remates
        bancarios, o, apartamentos en oportunidad, tumbarlo todo por completo,
        transformarlo con las ultimas tendencias y materiales para sacarlo de nuevo al
        mercado compitiendo con apartamentos nuevos.</p>
    <h3 style="margin: 0;">Ahora o nunca</h3>
    <p>Nace Constructora Soarco, sus siglas soluciones en arquitectura y construcción; para
        satisfacer las necesidades de los vecinos de Bogotá a nivel urbano y cuando
        requieren una segunda vivienda a las afueras de la gran urbe, nos hemos
        encaminado desde entonces por entender a nuestros clientes con casas
        unipersonales para sus familias a las afueras de Bogotá, “queremos ser grandes
        donde los grandes no están”, es decir, el mercado de las capitales de los
        departamentos esta captado por constructoras de gran jerarquía y reconocimiento,
        enfocadas en megaproyectos; pero reconocemos un nicho de mercado que está
        creciendo vertiginosamente hace ya unos años en municipios circundantes a las
        ciudades. Municipios que han tenido crecimiento sostenido, por considerarse cada
        día en una alternativa a las megaurbes. Es así, como en el 2015, iniciamos con el
        proyecto Condominio Campestre Refugio de los Santos, (19 Casas Campestres)
        ubicado en el municipio de La Mesa Cundinamarca. Cuenta con más de 12.000 m2
        entre urbanismo, redes y construcción de las viviendas, en este mismo periodo de
        tiempo se han elaborado más de 3.000 m2 en construcciones de casas campestres
        en toda la región del Tequendama, Cartagena y Boyacá.</p>
    <p>En el año 2021 adquirimos el lote en el casco urbano de La Mesa Cundinamarca,
        pensado para generar apartamentos y locales para atender la demanda creciente
        de nuevos habitantes de la región. Es así, como en el 2023 se expide la licencia de
        urbanismo y de construcción para 59 apartamentos y 26 locales en 3 torres de
        apartamentos y una plazoleta comercial.</p>
    <p>A medida que la empresa crece, la familia se mantiene unida, enfrentando juntos
        los desafíos del sector. Cada proyecto fue una oportunidad para aprender y mejorar,
        lo que permitió a la empresa adaptarse a las demandas cambiantes del mercado.
        Gracias a la dedicación y el esfuerzo colectivo, logramos establecer una reputación
        sólida basada en la confianza, la calidad y el compromiso social. La innovación se
        convirtió en parte de nuestra cultura empresarial. En resumen, la historia de
        nuestra empresa constructora es una prueba de que la unión familiar y la confianza
        son fuerzas poderosas que, cuando se combinan, pueden transformar visiones en
        realidades y generar un impacto poderoso.</p>
</section>

